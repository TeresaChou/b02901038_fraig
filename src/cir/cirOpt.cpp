// Remove unused gates
// DFS list should NOT be changed
// UNDEF, float and unused list may be changed
void
CirMgr::sweep()
{
	bool clear = false;
	while(!clear){
		clear = true;
		for(vector<unsigned>::iterator it=_unUsedList.begin(); it!=_unUsedList.end(); ) {
			if(removeGate(*it)){	
				_unUsedList.erase(it);
				clear = false;
			}
			else it++;
		}
		setUnUsedList(true);
	}
	_floatingList.clear();
	setFloatingList();
}

